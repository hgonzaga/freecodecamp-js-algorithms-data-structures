/*
Use Destructuring Assignment to Assign Variables from Arrays:
Use destructuring assignment to swap the values of a and b so that a receives the value stored in b, and b receives
the value stored in a.

- The value of a should be 6, after swapping.
- The value of b should be 8, after swapping.
- You should use array destructuring to swap a and b.
 */
