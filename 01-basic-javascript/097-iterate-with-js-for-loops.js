/*
Iterate with JavaScript For Loops:
Use a for loop to push the values 1 through 5 onto myArray.

- You should be using a for loop for this.
- myArray should equal [1, 2, 3, 4, 5].
 */
// Setup
const myArray = [];

// Only changed code below this line
for (let i = 1; i <= 5; i++) {
    myArray.push(i);
}